package MarvelPackage;

public class Operators {

	public static void main(String[] args) {
	
		
		int a=10;
		
		System.out.println(a++);//assign and increment
		System.out.println(a);
		System.out.println(++a);//increment and assign
		System.out.println(a);
		System.out.println(a--);
		System.out.println(--a);
		
		

	}

}
